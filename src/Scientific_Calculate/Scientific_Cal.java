    private void LoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoadActionPerformed
        // TODO add your handling code here:
        JFileChooser fs = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        
        int returnValue = fs.showOpenDialog(null);
        
        if (returnValue == JFileChooser.APPROVE_OPTION) { 
        File ls = fs.getSelectedFile();
        String result="";
        String line="";
        
        try (BufferedReader br = new BufferedReader(new FileReader(ls.getPath()))) {
                while ((line = br.readLine()) != null) {      
                // use comma as separator
                String[] formula = line.split(",");
                for(String s:formula){
                 result=result + s;
                }
                
                jtxtDisplayin.setText(result);

                }  

        } catch (IOException e) {
            e.printStackTrace();
        }

        }
      
    }//GEN-LAST:event_LoadActionPerformed

