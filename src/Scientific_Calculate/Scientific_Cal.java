    private void jMenuItem4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem4ActionPerformed
        // TODO add your handling code here:
         FileWriter writer;
           
        FormulaElement f = new FormulaElement(){};
        String str = jtxtDisplayin.getText();
        FormulaElement f2 = f.parseFormula(str); 
        String csv = f.getCSV();
        JFileChooser fs = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        fs.setFileFilter(new FileNameExtensionFilter("Comma Seperated Values (*.csv)", ".csv"));
        int returnValue = fs.showSaveDialog(null);
        
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            try {
                File selectedFile = fs.getSelectedFile();
                char buffer[]= new char[csv.length()];
                csv.getChars(0, csv.length(), buffer, 0);
                writer = new FileWriter(selectedFile.getAbsolutePath()+".csv");
                try {
                    writer.write(buffer);
                } catch (IOException ex) {
                    Logger.getLogger(Scientific_Cal.class.getName()).log(Level.SEVERE, null, ex);
                }
                writer.close();
                System.out.println(selectedFile.getAbsolutePath());
            } catch (IOException ex) {
                Logger.getLogger(Scientific_Cal.class.getName()).log(Level.SEVERE, null, ex);
            }
		}
    }//GEN-LAST:event_jMenuItem4ActionPerformed

