    private void jbtnEqualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnEqualActionPerformed
    try{ 
        String str = jtxtDisplayin.getText();
    
        save.add(str);
        FormulaElement f = new FormulaElement() {};
        FormulaElement f2 = f.parseFormula(str); 
        for (String i : nameValue.keySet()) {
        f2.setVariableValue(i,Double.valueOf(nameValue.get(i)));
        }
       
        String answer = Float.toString((float) f2.evaluate());
        jtxtDisplayout.setText(answer);
        String saved = "";
        for(Object i:save){
          saved = saved + i.toString()+ "\n";
        }
        jTextAreaSaveArera.setText(saved);
        nameValue.clear();
    }catch(Exception e){
     jtxtDisplayout.setText("Syntax Error");
    }
        
    }//GEN-LAST:event_jbtnEqualActionPerformed

